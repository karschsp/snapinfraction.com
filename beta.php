        <h3>Bio
        </h3>
        <p>
          <strong>Chin Music
          </strong>, the debut EP from Philadelphia-area duo Snap Infraction, is a tight collection of 5 original hook-heavy songs drawing on the band's wide array of influences. Written over a span of ten years, and recorded mostly at home in the span of ten months, the EP is a straight-forward rock record with a nod toward the classics: Beatles, Stones and Zeppelin, without relying heavily on any particular influence.
        </p>
        <p>Songwriter and multi-instrumentalist Steve Karsch explains: "At the end of the day, what we really care about is the song. It may have a country flavor to it, a heavy blues rock vibe, or a sickenly-sweet pop melody; if it's a good song, if it's a song we both like, it's a song for Snap."
        </p>
        <p>After years of playing together in bands up and down the east coast, Steve and drummer/audio engineer Tony Iannuzzi grew tired of the same old band politics, and vowed to start a project on their own terms, they just didn't think it would take this long.
        </p>
        <p>"The plan for Snap Infraction has always been that there is no plan. We may never release another record, or we could start recording a new album tomorrow...or we could wait another decade. You never know."
        </p>
        <p>After writing and demoing up to 50 songs, the pair decided it was finally time to buckle down and introduce the world to Snap Infraction. Not willing to compromise on their vision, the duo decided to take on all aspects of production, from writing and performing to engineering and mixing. When it came time to put the finishing touches on the record, the pair called in power-pop legend Cliff Hillis to help with final mixes and mastering.
        </p>
        <p>"We've always been fans of the classics," Karsch continues. "And of the guitar-bass-drums combination. No frills, nothing fancy, just good, solid rock n roll."
        </p>
        <p>The result is Chin Music, a short trip through the past half-century of rock n roll with a look towards what may lie ahead.
        </p>
